	SUBROUTINE ARMIN (NDIM,N,D,IA,IB,T,KT)
	DIMENSION D(NDIM,N),IA(N),IB(N),T(N),KT(N)
	DO 10 I=2,N
	T(I)=D(1,I)
10	KT(I)=1
	J=0
20	DINF=1.0E20
	DO 30 I=2,N
	IF (T(I).EQ.0.0) GOTO 30
	IF (T(I).GE.DINF) GOTO 30
	INF=I
	DINF=T(I)
30	CONTINUE
	J=J+1
	IA(J)=KT(INF)
	IB(J)=INF
	IF (J.EQ.N-1) GOTO 50
	T(INF)=0.0
	DO 40 I=2,N
	IF (T(I).EQ.0.0) GOTO 40
	DI=D(I,INF)
	IF (T(I).LE.DI) GOTO 40
	KT(I)=INF
	T(I)=DI
40	CONTINUE
	GOTO 20
50	RETURN
	END
