      SUBROUTINE CORRL(NDIM,KDIM,N,K,X,P,COR,XMOY,SX,KOMPT)
      DIMENSION X(NDIM,KDIM),P(NDIM),COR(KDIM,KDIM)
      DIMENSION SX(KDIM),XMOY(KDIM)
      MODE=0
      DO 20 I=1,N
      PI=P(I)
      DO 10 J=1,K
10    SX(J)=X(I,J)
      CALL COMAJ(MODE,NDIM,KDIM,K,COR,XMOY,SX,PI,SOMP,KOMPT)
20    CONTINUE
      MODE=2
      CALL COMAJ(MODE,NDIM,KDIM,K,COR,XMOY,SX,PI,SOMP,KOMPT)
      RETURN
      END
